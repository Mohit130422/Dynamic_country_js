$(document).ready(function () {
    $(".question").hide();
    $('input[type="radio"]').click(function () {
        var inputValue = $(this).attr("value");
        var targetBox = $("." + inputValue);
        $(".post_content").not(targetBox).hide();
        $(targetBox).show();
    });

    $("#bell").click(function () {
        $("#box").toggle();
    });
    $(".action-btn").click(function () {
        $("#act-box").toggle()
    });
    $("#close, main").click(function () {
        $("#box").hide();
        $(".sidebar").removeClass('openside');
        $("#act-box").hide();
    });
    $(".btnmenu").click(function(){
        $(".sidebar").toggleClass('openside');
    });
    $(".option").click(function(){
        $("#opt-box").toggle();
    });
    $("#option1").click(function(){
        $("#opt-box1").toggle();
    });
    $("#option2").click(function(){
        $("#opt-box2").toggle();
    });
    $("#share-course").click(function(){
        $("#share-box").css("display","flex");
    });
    $("#share-btn").click(function(){
        $("#share-modal").css("display","flex");
    });
    $("#close-share").click(function(){
        $("#share-modal").hide();
    });
    $('#recommended-slide').owlCarousel({
        loop:true,
        margin:10,
        nav:true,
        dots:false,
        lazyLoad:true,
        responsive:{
            0:{
                items:1
            },
            600:{
                items:2
            },
            1000:{
                items:3
            }
        }
    })
    $('#trending-slide').owlCarousel({
        loop:true,
        margin:10,
        nav:true,
        dots:false,
        lazyLoad:true,
        responsive:{
            0:{
                items:1
            },
            600:{
                items:2
            },
            1000:{
                items:3
            }
        }
    })
    $('#teachers-slide').owlCarousel({
        loop:true,
        margin:10,
        nav:true,
        dots:false,
        lazyLoad:true,
        responsive:{
            0:{
                items:1
            },
            600:{
                items:2
            },
            1000:{
                items:3
            }
        }
    })
    $('#instructor-slide').owlCarousel({
        loop:true,
        margin:10,
        nav:false,
        dots:false,
        lazyLoad:true,
        dots:true,
        responsive:{
            0:{
                items:1
            },
            600:{
                items:1
            },
            1000:{
                items:1
            }
        }
    })

});